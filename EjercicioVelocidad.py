#Calcula velocidad promedio, dado el tiempo y la distancia

distancia= int(input("Teclea la distancia de tu viaje en kms enteros: "))
tiempo= int(input("Teclea el tiempo de tu viaje en horas enteras: "))

velocidad= distancia / tiempo
print("La velocidad promedio:", velocidad, "km/h")
